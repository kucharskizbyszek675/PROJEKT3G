function opinia1(){
    var nick = document.getElementById("User").value
    var ogrze = document.getElementById("ogame").value
    document.getElementById("txt").innerHTML = "Dziękujemy za twoją opinie " + nick + ". Miło nam że korzystasz z naszego serwisu"
    if(nick == "" || nick == null){
        alert("WYPEŁNIJ WSZYSTKIE POLA")
        document.getElementById("txt").innerHTML = ""
    }
    if(ogrze == "" || ogrze == null){
        alert("WYPEŁNIJ WSZYSTKIE POLA")
        document.getElementById("txt").innerHTML = ""
    }
}
